// 自動產生亂數 產出長度 n 的 陣列 (slice)
func IntSlice(n int) []int {
	rand.Seed(time.Now().UnixNano())
	nums := make([]int, 0, n)
	for i := 0; i < n; i++ {
		nums = append(nums, int(rand.Intn(100)))
	}
	return nums
}
